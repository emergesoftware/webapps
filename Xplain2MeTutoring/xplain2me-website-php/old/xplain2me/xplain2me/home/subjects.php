                        <h2><strong>Our Lesson breakdown </strong></h2>
                        <p>According to a survey carried out by the Xplain2me team, primary students quickly lose focus 
and concentration during a tutorial while high school and tertiary level leaners take long and also need a bit longer to consolidate on all aspects taught. Our lessons are therefore 1 hour long for Primary Leaners and 1hr 30min for high school and university level students.
</p>
                        <br>
                        <p>Our single lesson is broken down into the following</p>
                        <li>Engaging the learner to draws his/her interest into the content of the lesson</li>
                        <li>Re visiting and revising homework if any, tutor addressing any challenges the learner is 
struggling with from previous work.
</li>
                        <li>Covering the work on agenda of the day with maximum student participation.</li>
                        <li>Tutor and student interaction on difficult components and tests or exam possibilities on the 
